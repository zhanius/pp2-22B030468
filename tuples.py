thistuple = ("apple", "banana", "cherry")
print(thistuple)

#length
thistuple = ("apple", "banana", "cherry")
print(len(thistuple))

#tuples with one item
thistuple = ("apple",)
print(type(thistuple))

#NOT a tuple
thistuple = ("apple")
print(type(thistuple))

#type
mytuple = ("apple", "banana", "cherry")
print(type(mytuple))

#the tuple() constructor
thistuple = tuple(("apple", "banana", "cherry")) # note the double round-brackets
print(thistuple)
